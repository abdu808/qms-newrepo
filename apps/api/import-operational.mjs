let seq = 1;
const code = () => `OPA-2026-${String(seq++).padStart(3,'0')}`;

async function run() {
  console.log('🚀 بدء استيراد الخطة التشغيلية 2026...\n');

  // ── جلب الأهداف الاستراتيجية من قاعدة البيانات ─────────────
  const goals = await prisma.strategicGoal.findMany({ select: { id: true, title: true } });
  const goalByTitle = Object.fromEntries(goals.map(g => [g.title, g.id]));
  console.log(`📌 وُجد ${goals.length} هدف استراتيجي في النظام\n`);

  // ── إدخال النشاطات ──────────────────────────────────────────
  console.log(`📋 إدخال ${ACTIVITIES.length} نشاطاً تشغيلياً...`);
  let inserted = 0, skipped = 0;

  for (const [title, kpi, target, budget, domainKey] of ACTIVITIES) {
    const [goalTitle, perspective] = DOMAIN_MAP[domainKey];
    const strategicGoalId = goalByTitle[goalTitle];

    if (!strategicGoalId) {
      console.warn(`  ⚠️  لم يُوجد الهدف: ${goalTitle}`);
      skipped++;
      continue;
    }

    const existing = await prisma.operationalActivity.findFirst({ where: { title, year: 2026 } });
    if (existing) {
      console.log(`  ⚠️  موجود: ${title.slice(0,45)}`);
      skipped++;
      continue;
    }

    const rec = await prisma.operationalActivity.create({
      data: {
        code:            code(),
        title,
        description:     `المؤشر: ${kpi} | المستهدف: ${target}`,
        perspective,
        year:            2026,
        startDate:       new Date('2026-01-01'),
        endDate:         new Date('2026-12-31'),
        budget:          budget,
        spent:           0,
        progress:        0,
        status:          'PLANNED',
        strategicGoal:   { connect: { id: strategicGoalId } },
      },
    });
    console.log(`  ✅  ${rec.code} — ${title.slice(0,50)}  [${(budget/1000).toFixed(0)}K]`);
    inserted++;
  }

  // ── ملخص ────────────────────────────────────────────────────
  const totalBudget = ACTIVITIES.reduce((s,a)=>s+a[3],0);
  console.log(`\n🎉 اكتمل الاستيراد!`);
  console.log(`   • نشاطات أُدخلت:  ${inserted}`);
  console.log(`   • تجاوزت (موجودة): ${skipped}`);
  console.log(`   • إجمالي الميزانية: ${totalBudget.toLocaleString('ar-SA')} ريال`);
}

run()
  .catch(e => { console.error('❌ خطأ:', e.message); process.exit(1); })
  .finally(() => prisma.$disconnect());
