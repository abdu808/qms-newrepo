async function run() {
  let total = 0;

  // helper
  const ins = async (label, fn) => {
    try { const r = await fn(); console.log(`  ✅ ${label}`); total++; return r; }
    catch (e) { if (e.code === 'P2002') console.log(`  ⚠️  موجود: ${label}`);
                else console.log(`  ❌ ${label}: ${e.message}`); }
  };

  // 4.1 SWOT
  console.log('\n📊 4.1 — SWOT Analysis (12 عنصر)');
  for (const s of SWOT) await ins(s.code + ' ' + s.type, () => p.swotItem.create({ data: s }));

  // 4.2 Interested Parties
  console.log('\n🤝 4.2 — الأطراف ذات العلاقة (8 أطراف)');
  for (const pt of PARTIES) await ins(pt.code + ' ' + pt.name, () => p.interestedParty.create({ data: pt }));

  // 4.4 Processes
  console.log('\n🔗 4.4 — خريطة العمليات (9 عمليات)');
  for (const pr of PROCESSES) await ins(pr.code + ' ' + pr.name, () => p.process.create({ data: pr }));

  // 5.2 Quality Policy
  console.log('\n📜 5.2 — سياسة الجودة');
  const existing = await p.qualityPolicy.findFirst({ where: { version: '1.0' } });
  if (existing) console.log('  ⚠️  سياسة الجودة موجودة مسبقاً');
  else { await p.qualityPolicy.create({ data: QUALITY_POLICY }); console.log('  ✅ سياسة الجودة أُنشئت'); total++; }

  // 6.1 Risks
  console.log('\n⚠️  6.1 — المخاطر والفرص (10 عناصر)');
  const adminUser = { connect: { id: ADMIN_ID } };
  for (const r of RISKS) {
    await ins(r.code + ' ' + r.title, () => p.risk.create({
      data: { ...r, score: r.probability * r.impact, createdBy: adminUser }
    }));
  }

  // 7.2 Competence
  console.log('\n🧑‍🎓 7.2 — متطلبات الكفاءة (8 أدوار)');
  for (const c of COMPETENCE) await ins(c.code + ' ' + c.jobTitle, () => p.competenceRequirement.create({ data: c }));

  // 7.4 Communication
  console.log('\n📣 7.4 — خطة الاتصال (10 عناصر)');
  for (const c of COMMS) await ins(c.code + ' ' + c.topic, () => p.communicationPlan.create({ data: c }));

  console.log(`\n🎉 اكتمل البذر! أُضيف ${total} سجلاً.`);
  console.log('\n📌 المتبقي للمرحلة التالية (يحتاج مستندات فعلية):');
  console.log('  7.5 — الوثائق: سياسة الجودة + إجراءات تشغيل (PDF)');
  console.log('  8.4 — الموردون: تقييم الموردين الحاليين + اعتماد');
  console.log('  9.2 — التدقيق الداخلي: جدولة وتنفيذ التدقيق الأول');
  console.log('  9.3 — مراجعة الإدارة: عقد الاجتماع الأول وتوثيقه');
}

run()
  .catch(e => { console.error('❌ خطأ:', e.message); process.exit(1); })
  .finally(() => p.$disconnect());
